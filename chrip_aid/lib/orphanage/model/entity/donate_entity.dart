class DonateEntity {
  final String orphanageName;
  final String date;
  final String productName;
  final int price;
  final int count;
  final String message;

  DonateEntity({
    required this.orphanageName,
    required this.date,
    required this.productName,
    required this.price,
    required this.count,
    required this.message,
  });
}

